var lista = ['Nícolas', 'Ferreira', 'Leite', 18];

lista[1]; // Ferreira
lista.length; // 4
lista; //['Nícolas', 'Ferreira', 'Leite', 18];
lista.indexOf('Leite'); // 2
lista.indexOf('João'); // -1
lista.push('Pedro'); //['Nícolas', 'Ferreira', 'Leite', 18, 'Pedro'];
lista.pop(); // retorna e apaga o último item da lista
lista.shift(); // retorna e apaga o primeiro item da lista